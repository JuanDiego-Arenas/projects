    public void listarProductos() {
        DefaultTableModel dataModel = new DefaultTableModel(new Object[]{
            "Codigo", "Nombre", "Precio", "Inventario"}, 0);
        
        for(Producto p: baseDatos.getListaProductos().values()) {
            dataModel.addRow(new Object[]{
                p.getCodigo(),p.getNombre(),p.getPrecio(),p.getInventario()});
        }
        
        TblInventario.setModel(dataModel);
        
        //TblInventario.setEnabled(false);
        TblInventario.setDefaultEditor(Object.class, null);
    }

